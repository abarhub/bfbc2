public class Etoiles {

	private int bronze;
	private int argent;
	private int or;
	private int platine;

	public Etoiles() {
	}

	public int getBronze() {
		return bronze;
	}

	public void setBronze(int bronze) {
		this.bronze = bronze;
	}

	public int getArgent() {
		return argent;
	}

	public void setArgent(int argent) {
		this.argent = argent;
	}

	public int getOr() {
		return or;
	}

	public void setOr(int or) {
		this.or = or;
	}

	public int getPlatine() {
		return platine;
	}

	public void setPlatine(int platine) {
		this.platine = platine;
	}
}
